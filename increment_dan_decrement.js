//////////////////////// increment_dan_decrement.js
for (var angka = 1; angka <= 20; angka++) {
  console.log(angka +' -I love coding');
}
for (var angka = 20; angka >= 1; angka--) {
  console.log(angka +' -I will become fullstack developer');
}
